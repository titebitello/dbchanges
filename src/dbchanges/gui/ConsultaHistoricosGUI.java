    private void populateComboboxProject(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_populateComboboxProject
        try {
            ProjetosDAO projetoDao = new ProjetosDAO();
            List<ProjetosDTO> projetos = projetoDao.recuperarProjetosPorNomeComboBox();
            jcbProjetos.getSelectedItem();
            jcbProjetos.removeAll();

            for (ProjetosDTO projeto : projetos) {
                jcbProjetos.addItem(projeto);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaHistoricosGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_populateComboboxProject

    private void btnFeedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFeedbackActionPerformed
        HistoricosDTO historico = new HistoricosDTO();
        historico.setId(Integer.parseInt(jtfIdHistoricoFeedback.getText()));
        historico.setTempoReal(Integer.parseInt(jtfTempoRealFeedback.getText()));
        HistoricosDAO dao = new HistoricosDAO();
        dao.adicionaFeedback(historico);
        JOptionPane.showMessageDialog(null, "Historico " + jtfIdHistoricoFeedback.getText() + " atualizado com sucesso! ");
        jtfIdHistoricoFeedback.setText("");
        jtfTempoRealFeedback.setText("");
    }//GEN-LAST:event_btnFeedbackActionPerformed

    private void jtbProjetoHistoricosStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jtbProjetoHistoricosStateChanged
        try {
            ProjetosDTO projeto = (ProjetosDTO) jcbProjetos.getSelectedItem();
            HistoricosDAO historicoDao = new HistoricosDAO();
            List<HistoricosDTO> historicos = historicoDao.recuperarHistoricosJtable(projeto.getId());
            //historicos.size();
            DefaultTableModel modeloTable = (DefaultTableModel) jtbHistoricos.getModel();
            for (HistoricosDTO historico : historicos) {
                modeloTable.addRow(new Object[]{
                    historico.getId(),
                    historico.getDatahora(),
                    historico.getLinhas(),
                    historico.getColunas(),
                    historico.getFks(),
                    historico.getConstraints2(),
                    historico.getFunction2(),
                    historico.getTrigger2(),
                    historico.getIndex2(),
                    historico.getView2(),
                    historico.getTabela(),
                    historico.getTempoEstimado(),
                    historico.getTempoReal()
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jtbProjetoHistoricosStateChanged

